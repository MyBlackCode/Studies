
public class Figure {
	////////////////////////////POLA KLASY BEGIN///////////////////////////////////////////////////////
	private String color; 
	private double surface_area;
	private double circumference;
	////////////////////////////POLA KLASY END///////////////////////////////////////////////////////
	
	
	////////////////////////////KONSTRUKTORY BEGIN ///////////////////////////////////////////////////////
	public Figure()
	{
		color = "brak";
		surface_area = 0.00;
		circumference = 0.00;
	}
	
	public Figure(String Color, double Surface_area, double Circumference)
	{
		color = Color;
		surface_area = Surface_area;
		circumference = Circumference;
	}
	////////////////////////////KONSTRUKTORY END ///////////////////////////////////////////////////////
	

	////////////////////////////FUNKCJE USTAWIAJACE DANĄ W KLASIE BEGIN ///////////////////////////////////////////////////////
	public void setSurface(double newSurference) {surface_area = newSurference;}
	public void setCircumference(double newCircumference) {circumference = newCircumference;}
	////////////////////////////FUNKCJE USTAWIAJACE DANĄ W KLASIE END ///////////////////////////////////////////////////////
	
	
	////////////////////////////FUNKCJE ZWRACAJACA WARTOSC DANEJ Z KLASY BEGIN ///////////////////////////////////////////////////////
	public double getSurface() {return surface_area;}
	public double getCircumference(){ return circumference;} 
////////////////////////////FUNKCJE ZWRACAJACA WARTOSC DANEJ Z KLASY  ///////////////////////////////////////////////////////
	
	
	
	////////////////////////////WYSWIETLANIE DANYCH BEGIN ///////////////////////////////////////////////////////
	public void viewData()
	{
		System.out.println(color + " , ma obwod:" + circumference + ", a powierzchnia jego pola wynosi: "+ surface_area);
	}
	////////////////////////////WYSWIETLANIE DANYCH END ///////////////////////////////////////////////////////

}
